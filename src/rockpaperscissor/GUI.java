    private void scissorButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_scissorButtonActionPerformed
        // TODO add your handling code here:
        userScissor.setVisible(true);
        userPaper.setVisible(false);
        userDefault.setVisible(false);
        userRock.setVisible(false);

        user = "Scissor";
        comp = generateCompMove();

        if (comp == "Rock") {
            getRockImage();
            getCompWinsMsg();
        } else if (comp == "Scissor") {
            getScissorImage();
            getTieMsg();
        } else if (comp == "Paper") {
            getPaperImage();
            getUserWinsMsg();
        }
        showWinningMessage();
    }//GEN-LAST:event_scissorButtonActionPerformed

