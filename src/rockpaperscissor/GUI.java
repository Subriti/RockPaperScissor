    private String generateCompMove() {
        Random rand = new Random();
        // Generate random integers in range 0 to 2
        int random = rand.nextInt(3);
        switch (random) {
            case 0 -> {
                return "Rock";
            }
            case 1 -> {
                return "Paper";
            }
            case 2 -> {
                return "Scissor";
            }
            default -> {
            }
        }
        return null;
    }

    private void showWinningMessage() {
        if (Your_win == 10) {
            JOptionPane.showMessageDialog(rootPane, "Game Finished: YOU WON");
            System.out.println("Game Finished: YOU WON");
            System.exit(0);
        } else if (Comp_win == 10) {
            JOptionPane.showMessageDialog(rootPane, "Game Finished: COMPUTER WON");
            System.out.println("Game Finished: COMPUTER WON");
            System.exit(0);
        } else if (Tie == 10) {
            JOptionPane.showMessageDialog(rootPane, "Game Finished: TIE");
            System.out.println("Game Finished: TIE");
            System.exit(0);
        }
    }

